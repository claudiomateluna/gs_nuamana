<p><strong>La Rosa de los vientos</strong></p>



<p>La llamada “Rosa de los Vientos” es una representación que surgió de la marinería y representa básicamente los cuatro puntos cardinales (norte, sur, este, oeste) y los otros cuatro que hay entre ellos.</p>



<p>Esta forma de orientarse es útil en rasgos generales y se sigue usando, pero si se quiere tener más precisión, se usa un método numérico. Por supuesto, la Rosa de los Vientos abarca todo un círculo, por lo que puede ser dividido en grados, y todas las subdivisiones de éstos.</p>



<p>Cuando se usa esta forma de orientarse, a cada una de las direcciones que se marca o se dirige uno, se le llama azimut o lectura azimutal.</p>



<p>Como la numeración parte del norte y crece hacia el oriente, los azimutes para los puntos cardinales son norte (N), 0 o 360ª; este (E), 90º; sur (S), 180º; oeste (W), 270º.</p>



<p>El oeste no tiene como letra representativa la O para evitar confundirse con un cero y así se le pone la W, de la palabra alemana e inglesa para designar al mismo punto: “West”.</p>



