<h3 class="wp-block-heading" id="qué-es-la-brújula">¿Qué es la Brújula?</h3>



<p>Inventada por los chinos, la brújula no es más que una aguja imantada que responde al campo magnético de la Tierra. Por supuesto, es la brújula más sencilla pero las actuales tienen diferentes partes específicas que evitan muchos errores de medición debidos al factor humano.</p>



