<p>Como comisionado scout Roland Philipps escribió el libro "El Sistema de Patrullas", que se convirtió en un complemento de "Escultismo para muchachos", donde <a href="https://nuamana.com/nuanew/historia/biografias/baden-powell/" target="_blank" rel="noreferrer noopener">Robert Baden-Powell</a> explicaba los fundamentos del Escultismo.</p>



<p>En 1914 lo sorprendió la Primera Guerra Mundial, integrándose al ejército con el grado de Capitán. Murió el 7 de julio de 1916 luchando en la ribera del río Somme en Francia.</p>



<p>La casa de Roland Philipps en Londres, en la calle Stepney Green 29, conocida como The Roland House, fue oficina de la zona scout del Este de Londres, además de un museo en su memoria y un albergue para visitantes.</p>



<p>La casa fue vendida y es en la actualidad propiedad privada ajena al mundo scout; todo el patrimonio de Roland Philipps fue trasladado a las nuevas instalaciones del distrito de Tower Hamlets adquiridas con el beneficio de la venta de la casa.</p>



<h3 class="wp-block-heading" id="niñez-y-juventud">Niñez y juventud de Roland Philipps</h3>



<p>Roland Philipps nació el 27 de febrero de 1890, en el número 24 de Queen Anne’s Gate en Westminster, un distrito de Londres. Fue el segundo hijo de John Philipps, 1.er Vizconde St Davids y su esposa, Leonora Gerstenberg.</p>



<p>Roland Philipps recibió su educación en Winchester College y New College, Oxford, donde obtuvo su licenciatura en jurisprudencia, con honores. Como su padre, estudió derecho no tanto por vocación profesional sino como una introducción a la política.</p>



<p>La mayoría de las escuelas públicas más importantes apoyaban a comunidades pobres de las áreas industriales, para que los muchachos de las clases acomodadas se interesarán y tuvieran contacto directo con los muchachos menos afortunados.</p>



<p>Esta experiencia era de gran valor, ya que ayudaba a los jóvenes a ver con entendimiento los grandes problemas que encontrarían al asumir sus responsabilidades ciudadanas.</p>



<p><a href="http://www.manualscout.cl/ficha-054.htm" target="_blank" rel="noreferrer noopener">Roland Philipps</a> inmediatamente se sintió atraído por esta labor ya que estaba estrechamente ligada con las actividades filantrópicas que su madre realizaba y en las cuales siempre estuvo dispuesto a ayudarle.</p>



<p>Roland Philipps fue educado en una típica familia de clase alta y aquellos quienes lo conocieron no se habrían sorprendido si Roland Philipps hubiese elegido una carrera política. Su posición estaba con la fracción radical del Partido Liberal y llegó al punto de convertirse en postulante a candidato parlamentario por el sur de Glamorgan.</p>



<h3 class="wp-block-heading" id="carrera-política">Carrera Política</h3>



<figure class="wp-block-image alignright is-resized"><img src="https://raw.githubusercontent.com/claudiomateluna/nua_mana/gh-pages/uploads/biografia_RollandPhilipps_1.png" alt="Roland Philipps" style="width:360px"/></figure>



<p>La vida política de Roland Philipps fue muy notable, destacándose principalmente su oratoria. Roland Philipps era en palabras del mismo Robert Baden-Powell, “un fanático de los ideales scouts”, aunque su gran interés por el movimiento scout no fue motivo para separarlo de su actividad política.</p>



<p>Roland Philipps tenía muchos deseos de ingresar al Parlamento e hizo su debut político proponiéndose como candidato liberal por el este de Carmarthenshire.</p>



<p>Siendo un ferviente entusiasta a favor de un gobierno interno en Irlanda, así como por el desestablecimiento galés, contó con el apoyo de Sir David Lloyd George, líder de la fracción de izquierda del Partido Liberal, sin embargo, Roland Philipps fracasó al asegurar la nominación, debido a que se dio preferencia a un candidato que hablaba galés.</p>



<p>En uno de sus discursos, Roland Philipps expresó su simpatía por la reforma social y argumentó que no volvería a vivir en el oeste de Londres con personas de su misma clase social.</p>



<p>Había determinado establecer su residencia entre los pobres de Bethnal Green y no tenía en mente cambiar de opinión ya que encontraba este lugar “mucho más alegre”.</p>



<p>A pesar de haber perdido la nominación como candidato liberal por el este de Carmarthenshire, en 1912 fue reconocido por amplia mayoría como candidato por el sur de Glamorgan. Su gran personalidad y trato lo hicieron popular entre todos los estratos sociales.</p>



<p>De haber vivido, habría asegurado la elección, ya que tenía una gran visión con respecto a la reconciliación del socialismo y liberalismo. Persuadía a su audiencia, asegurando que a pesar de los distintos métodos de estas dos ideologías, no existía gran diferencia en sus objetivos.</p>



<p>Mientras tanto, el trabajo social de Roland Philipps en el este de Londres tomó un giro religioso ya que se le podía encontrar dando discursos a grandes audiencias en la Misión Laboral, donde enfatizaba la necesidad de simpatizar con todos nuestros hermanos y condenaba el espíritu de crítica.</p>



<h3 class="wp-block-heading" id="labor-social">Labor Social</h3>



<p>A la edad de 19 años Roland Philipps visitó por primera vez el este de Londres y se puso a disposición de la Sociedad de Caridad para quienes realizó amplia labor, además de ser muy activo en el trabajo parroquial.</p>



<p>Roland Philipps era miembro del sindicato y en sus debates desarrolló el don de la oratoria que había heredado de sus ancestros. Fue durante su estadía en Oxford cuando visitó por primera vez los barrios bajos del este de Londres. Se impresionó tanto por las necesidades de los trabajadores de esta área que en su último año en Oxford pasó la mayor parte de sus vacaciones en la Casa Universitaria de Asistencia de Bethnal Green.</p>



<p>Desde pequeño Roland Philipps siempre estuvo involucrado a fondo en todo tipo de labor relacionada con niños o gente mayor. Su primer discurso lo dio a la edad de 15 años en una reunión escolar en Letterston, en el norte de Pembrokeshire.</p>



<p>En julio de 1911, después de graduarse en las licenciaturas de historia y derecho en Oxford se mudó a Liverpool por seis meses. En la compañía naviera Pacific Steam Navigation Company aprendió el arte de la navegación.</p>



<p>Todas las tardes durante su estancia en Liverpool, Roland Philipps, las dedicó a labor filantrópica de distintos tipos y gran parte de su tiempo lo destinó a prestar ayuda en las misiones de marinos y hombres del pueblo, quienes conforman en gran parte la población más pobre de Liverpool.</p>



<p>Roland Philipps también trabajó mucho para los scouts de Liverpool y en el mes de marzo pronunció un discurso en una concurrida reunión scout en Runcorn.</p>



<p>En abril de 1912 regresó a Londres y trabajó por un corto periodo en las oficinas de la compañía naviera Union Castle y decididamente determinó vivir, mientras estuviera libre y pudiera hacerlo, en la bruma de los barrios bajos de Bethnal Green.</p>



<h3 class="wp-block-heading" id="el-este-de-londres">El Este de Londres</h3>



<p>Cuatro noches a la semana visitaba el Club de Trabajadores de Oxford con el que estuvo estrechamente relacionado por más de un año. El club contaba con más de 300 miembros y aproximadamente 150 de ellos se reunían todas las noches a jugar billar, ajedrez, damas, dominó y diversos juegos de mesa.</p>



<figure class="wp-block-image alignleft is-resized"><img src="https://raw.githubusercontent.com/claudiomateluna/nua_mana/gh-pages/uploads/biografia_RollandPhilipps_3.png" alt="" style="width:360px"/></figure>



<p>Las otras dos noches de la semana Roland Philipps las ocupaba en visitar los lugares de reunión de las 48 tropas de scouts que conformaban el Distrito del Este de Londres como Hackney, Stepney y Poplar. Su trabajo consistía en pasar revista a los muchachos, dar instrucción en lo que se requiriese, asesorar a los scouters y en general trabajar para mantener un alto nivel de adiestramiento en primeros auxilios, rescate, incendios, cocina y otras prácticas propias de los scouts.</p>



<p>Como secretario de la Sociedad de Trabajo Rural para Muchachos en Gales Roland Philipps tenía todos los días una gran cantidad de trabajo administrativo qué hacer, aparte de su labor en las noches con los scouts y el Club de Trabajadores.</p>



<p>Asimismo Roland Philipps participó con “Niños de la Calle” y con las “Casas del Dr. Bernard.” Varios domingos por la noche dio pláticas a más de doscientos muchachos del Dr. Bernardo en su amplia casa-hogar de Stepney Causeway.</p>



<p>También trabajó para algunos clubes de muchachos y en su tiempo libre realizaba visitas al Hospital de Londres y a las enfermerías de Bethnal Green y Hackney.</p>



<p>Todos los domingos por la tarde, cuando estaba en Londres, tomaba parte en una gran reunión de té que ofrecía la viuda del doctor Bernard en Stepney, para 1,400 de los niños más pobres de Inglaterra. Estos niños fluctuaban entre los dos meses y doce años de edad y se alimentaba a 600 o 700 de ellos al mismo tiempo. La mayoría de estos niños andrajosos daban un triste aspecto.</p>



<p>Probablemente una de las cosas que disfrutaba más era el trabajo que realizaba en la temporada de pizca de lúpulo (utilizado para la fermentación de la cerveza) en Kent. Todos los veranos durante algunas semanas, el soleado campo de Kent era invadido por cientos de londinenses necesitados junto con sus familias para la pizca de lúpulo. Jóvenes alumnos de la universidad bajaban a los campos de lúpulo y vivían en tiendas de campaña junto con los trabajadores para ayudarlos en su trabajo, apoyarlos con lecturas y recitales en su tiempo libre, así como darles el auxilio médico necesario. Un año Roland Philipps pasó una semana en tienda de campaña en Paddock Wood y otro año una noche en East Fawleigh.</p>



<p>Como es de suponerse todo este trabajo no sólo mantenía a Roland Philipps alejado de las delicias y lujos del oeste de Londres (de hecho, en muy raras ocasiones se le veía en esta área de la ciudad con excepción de las visitas que en la medida de lo posible hacía a sus padres a quienes tenía gran estima) sino también lo imposibilitaba para planear algún descanso de fin semana fuera de la ciudad.</p>



<p>Roland Philipps se caracterizaba por ser una persona feliz. Estaba profundamente ligado a la vida del este de Londres que en rara ocasión dejaba, sólo para visitar muy de vez en cuando su tierra natal en las montañas de Cymru.</p>



<p>Ser niño en el este de Londres era peligroso ya fuera por el crimen o por los riesgos de salud inherentes a la naturaleza de los alrededores. Había muchas madres y padres (particularmente madres) que tenían grandes expectativas para sus hijos y a quienes les daban una educación de la que estaban orgullosos. Sin embargo, las pocas áreas verdes estaban a varias millas de distancia del este de Londres, de tal forma que el único contacto existente era con callejones oscuros y calles transitadas.</p>



<p>Durante la vorágine de los últimos años de la primera década de ese siglo llegaron los scouts. En 1912 apareció en escena un Comisionado Asistente para el este y noreste de Londres, uno de los hombres más sobresalientes que jamás haya portado el uniforme scout, Roland Philipps.</p>



<h3 class="wp-block-heading" id="el-escultismo">El Escultismo</h3>



<p>Sin embargo en 1911 un encuentro casual cambió la dirección de su vida.</p>



<figure class="wp-block-image alignright is-resized"><img src="https://raw.githubusercontent.com/claudiomateluna/nua_mana/gh-pages/uploads/biografia_RollandPhilipps_2.png" alt="Sistema de Patrullas de Rolland Philipps" style="width:360px"/></figure>



<p>Después de dejar Oxford, Roland Philipps viajó a Liverpool donde vivió por seis meses y trabajó en las oficinas de la Compañía inglesa de Vapores con el objeto de aprender la técnica de la navegación. Un fin de semana durante una caminata en el campo encontró a algunos miembros de la Cuarta Tropa de Blundellsands, al platicar con ellos se enteró que les hacían falta líderes y se ofreció a ayudar. A la edad de 21 años Roland Philipps había encontrado el trabajo que regiría su corta y trágica vida.</p>



<p>A su regreso en Londres donde trabajó por un tiempo en las oficinas de la compañía naviera Union Castle, renovó su interés por el este de Londres y decide vivir, mientras pueda hacerlo en los barrios de Bethnal Green.</p>



<p>Roland Philipps llegó a ser Comisionado Scout por el este y noreste de Londres y Comisionado Asistente por Gales, sin embargo antes de asumir estos cargos personalmente perfeccionó su conocimiento sobre el escultismo hasta llegar a ser un dirigente scout ideal.</p>



<p>De hecho fue uno de los mejores exponentes del arte scout y escribió diversos libros populares en el área. Uno de los más importantes fue “El Sistema de Patrullas” que habla sobre el trabajo de la patrulla scout.</p>



<p>En la serie de “Cartas a un Guía de Patrulla” expone los principios de la ley scout y da consejo sobre la preparación de pie tiernos para pasar las pruebas requeridas. Este último libro es en sí un manual de entrenamiento escrito con tal sencillez y objetividad que muestra lo bien que comprendía la mentalidad de los muchachos.</p>



<p>Estos pequeños libros reflejan sus ideas y aún hoy en día pueden leerse con interés dada la sabiduría plasmada en el estilo y estructura de ese tiempo.</p>



<p>Roland se convirtió en una especie de misionero scout. Durante dos años trabajó motivando el crecimiento del movimiento. Tenía un don especial para encontrar líderes y fundar tropas. Insistía <strong>sobre todas las cosas que el Sistema de Patrulla era la esencia del escultismo</strong>.</p>



<p>Para valorar verdaderamente a Roland es esencial comprender su profunda fe cristiana. La oración fue fundamento de sus acciones y vio en el escultismo una manera de prestar servicio a los jóvenes, ideología que compaginaba totalmente con la fe que él tenía.</p>



<p>Esto no quiere decir que fuera intolerante en su anglicismo, por el contrario, creía firmemente que un muchacho carecería de un aspecto de vital importancia si no tenía una formación religiosa en su vida, sobre todo en un lugar como el este de Londres, donde el muchacho podía acercarse a la tradición cristiana, judaica, islámica o a cualquier otro tipo de creencia de esa área en particular.</p>



<h3 class="wp-block-heading" id="la-primera-guerra-mundial">La Primera Guerra Mundial</h3>



<p>A pesar de que poca gente lo pudo intuir, el mundo que Roland y sus scouts del este de Londres conocían se desmoronaría completamente, se acercaba el terrible año de 1914.</p>



<p>En este año, mientras estaba involucrado en su labor filantrópica estalló la gran guerra.</p>



<p>Apegado a la Ley scout su primer reacción fue prestar lealtad a su Rey y a su patria. Escribió a sus seguidores políticos en Cardiff:</p>



<blockquote class="wp-block-quote">
<p>“Como ustedes saben, siempre he apoyado las causas de paz, sin embargo, uno no puede ayudar esta causa dejando a Europa a merced de una tiranía militar. He sido un acérrimo oponente al reclutamiento y un creyente del servicio voluntario y es por quienes creen en este servicio cuando hay que demostrar que cuando nuestra nación nos necesita, éste, no sólo se vuelve voluntario, sino que se debe prestar con gusto. En este crucial momento de crisis nacional he hecho lo que creo es mi deber y me he enlistado en el ejército de Lord Kitchener. En concreto, después de haber participado un año con el cuerpo de entrenamiento de oficiales durante mi estancia en Oxford, parece ser que esta es la manera en que mejor puedo ayudar a mi patria.”</p>
</blockquote>



<p>Antes de ir a su entrenamiento hizo una última aportación a su amado este de Londres. Compró la casa en el número 29 de Stepney Green como centro de la labor que había iniciado y que en ese momento dejaba en manos de otros.</p>



<p>Ingresó al campo de entrenamiento en Didcot y a su debido tiempo fue nombrado Teniente Segundo y asignado al Noveno Batallón Real de Fusileros en el cual varios de sus muchachos scouts de Bethenal Green se habían enlistado.</p>



<p>Fue enviado a Francia en abril de 1915, sin embargo él y su hermano Colwyn obtuvieron un permiso para estar presentes en el funeral de su madre que falleció el 30 de marzo.</p>



<p>Fue elevado a rango de Capitán a principios de año y al acaecer la muerte de su hermano en el mes de mayo heredó la baronía de su padre.</p>



<p>Tomó parte en el ataque al Reducto de Hohensollern al sur de La Basée, en el que su bien entrenado y bien dirigido batallón tuvo una destacada participación. En esta batalla el Capitán Roland Philipps se distinguió de sobremanera. Fue condecorado con la Cruz Militar y el reporte oficial de sus servicios fue publicado en La Gaceta de Londres en donde se subrayó “en reconocimiento a su manifiesta gallardía y devoción al deber”.</p>



<p>A pesar de estar severamente herido mantuvo a sus hombres bajo control, habiendo el mismo matado a cuatro enemigos con su revólver. Se mantuvo en su puesto y repelió tres ataques. Recibió tres heridas de bala en la batalla y una grave cortada.</p>



<p>Al dar cuenta de los hechos de este suceso a Sir <a href="https://nuamana.com/nuanew/historia/biografias/baden-powell/" target="_blank" rel="noreferrer noopener">Robert Baden-Powell</a> le mencionó que esas tres heridas le sirvieron para recordar los tres puntos de la promesa scout.</p>



<p>Durante este reposo atendió a una investidura en el Palacio de Buckingham y ahí mismo fue condecorado por el Rey con la Cruz Militar en reconocimiento a su gallardía. Al agradecer la felicitación de un amigo escribió:</p>



<blockquote class="wp-block-quote">
<p>“… el honor pertenece a mis hombres quienes fueron valientes y alegres, más aún de lo que las mismas palabras puedan describir. Por lo que toca a mí, tuve la alegría y gran fortuna de ser su líder.”</p>
</blockquote>



<p>Se recuperó rápidamente y regresó a casa para tomar un breve descanso antes de reunirse de nueva cuenta con su batallón en servicio.</p>



<p>Roland instrumentó en las fuerzas armadas las mismas habilidades que había utilizado en su servicio dentro del escultismo. Su desempeño fue distinguido y sus permisos de franco los pasaba en Inglaterra donde se propuso como objetivo personal, que siempre cumplió, fundar por lo menos una tropa scout cada vez que estuviera de visita.</p>



<h3 class="wp-block-heading" id="fallecimiento">Fallecimiento</h3>



<p>El Capitán Roland Philipps murió el 7 de julio de 1916 en la Batalla del Somme en un ataque a las trincheras alemanas resguardadas por la guardia prusiana.</p>



<p>La Batalla del Somme de 1916 fue una de las más largas y sangrientas de la Primera Guerra Mundial, con más de un millón de bajas entre ambos bandos. Las fuerzas británicas y francesas intentaron romper las líneas alemanas a lo largo de un frente de 40 km al norte y al sur del río Somme, en el norte de Francia.</p>



<p>El principal propósito de la batalla era distraer a las tropas germanas de la Batalla de Verdún; sin embargo, las bajas de la batalla del Somme terminaron siendo superiores a las de esta última.</p>



<p>La batalla es recordada principalmente por su primer día, 1 de julio de 1916, en el que los británicos sufrieron 57.740 bajas, de las cuales 19.240 fueron mortales. Constituye la batalla más sangrienta en la historia del Ejército Británico.</p>



<p>Una carta escrita por Roland Philipps a un amigo muy cercano en la tarde del “Gran ataque” inicia “Un vivac feliz antes de la batalla” y termina “Nada puede separarnos del amor de Dios que se encuentra en nuestro Señor Jesucristo”.</p>



<p>En otra carta escrita ya entrada la noche del 6 de julio dice:</p>



<blockquote class="wp-block-quote">
<p>“Adiós. Nuestra oportunidad suprema de servir a nuestro país ha llegado. Mi vida ha sido muy feliz y no habrá obús o bala que pueda terminar con ella. Sólo ruego que a lo largo de la misma pueda permanecer cerca de Dios.”</p>
</blockquote>



<p>En julio de 1916 dio inicio la masacre que conocemos como la Batalla del Somme y el 7 del mismo mes cerca de la villa de Ovillers, Roland Philipps se convirtió en otro nombre de la aparentemente interminable lista de aquellos “caídos en batalla”.</p>



<p>Un testigo describió como sólo seis o siete de los 220 hombres a cargo de Roland sobrevivieron ilesos ese día, el resto murieron o quedaron heridos. La humanidad aún no ha podido reproducir un pasaje tan vívido del infierno en la tierra como lo fue la Primera Guerra Mundial.</p>



