<h3 class="wp-block-heading" id="partes-de-la-brújula">Partes de la Brújula</h3>



<p>Las partes son: Base de plástico, Anillo giratorio graduado, Aguja magnética, Flecha orientadora y sus líneas auxiliares, Punto de lectura, Flecha de dirección de viaje y sus líneas auxiliares.</p>



<p><strong>Base de Plástico</strong></p>



<p>Todo el cuerpo de la brújula está sostenido por una base de plástico resistente y transparente. Ahí están las demás piezas y generalmente uno olvida que la base está ahí.</p>



<p>Tiene por sí misma sus privilegios, como una a tres escalas de medición y a veces una lupa, pero, sobre todo, la flecha de dirección de viaje.</p>



<p>Es importante que la base sea transparente para que permita ver el mapa sin dificultad.</p>



<p><strong>Anillo Giratorio</strong></p>



<p>La parte más notoria en la base de plástico es un cilindro aplastado. Sobre él hay un anillo giratorio que tiene divisiones cada determinada distancia y que completan un círculo de 360 grados, lo que convierte a esta escala en un transportador que puede medir ángulos.</p>



<p>La brújula estándar tienen una división mínima de dos grados y son lo suficientemente buenas como para hacer viajes de mediana longitud sin muchas correcciones. Es preferible que la brújula tenga esta división lo más pequeña posible para evitar errores adicionales.</p>



<p>Existen brújulas con división de cinco grados que se pusieron de moda de un día al otro, quizá sólo por ser un poco más baratas. Sencillamente no sirven en la mayoría de los casos en que deben ser usadas porque arrojan un error de medición demasiado alto.</p>



<p><strong>Aguja Magnética</strong></p>



<p>Dentro del cilindro está la aguja magnética, inmersa en aceite para que el movimiento de inercia sea frenado lo más rápidamente pero sin detener el avance de la aguja. Como ya dijimos, la aguja es la parte más importante de toda la brújula pues aún si se rompe toda la base y el cilindro, se puede usar, aunque con muchas más dificultades.</p>



<p><strong>Flecha Orientadora</strong></p>



<p>La flecha orientadora está también dentro del cilindro pero por debajo de la aguja magnética. Generalmente es una doble línea que semeja una gran flecha, con la punta señalada claramente por tres líneas que pretenden ser movimiento continuo.</p>



<p>A los lados de esta flecha hay líneas que son paralelas a esta flecha y que son auxiliares.</p>



<p><strong>Punto de Lectura</strong></p>



<p>En la parte superior del cilindro, sobre la numeración de las divisiones mínimas del transportador, existe un punto, generalmente de color blanco. Ahí se realizará cualquier lectura que se haga con la brújula.</p>



<p><strong>Flecha de Dirección de Viaje</strong></p>



<p>Es una línea que atraviesa la mayor parte de la base de plástico y termina con una flecha sencilla. A sus lados también hay líneas auxiliares, pero diferentes de la flecha orientadora.</p>



<h3 class="wp-block-heading" id="qué-es-lo-que-mide-una-brújula">¿Qué es lo que Mide LA Brújula?</h3>



<p>La brújula mide ángulos horizontales con respecto a una línea que es fija: la línea magnética de la Tierra. La parte roja de la aguja se dirigirá a la parte norte del campo magnético mientras la blanca se dirigirá al sur.</p>



<p>Es muy importante remarcar que la brújula no apunta al norte, sino que sigue las líneas magnéticas. Lo que mide, entonces son ángulos horizontales con respecto a la línea magnética en la que estemos.</p>



<p><strong>Líneas Magnéticas</strong></p>



<p>El magnetismo terrestre no es constante en toda la superficie. Se altera por yacimientos de minerales y masas de agua, por ejemplo. Si quisiéramos cortar una manzana con gajos que siguieran la forma de estas líneas, no tendríamos formas simétricas, sino bastante irregulares.</p>



<p>Hay dos tipos de líneas magnéticas: las agónicas [de a, privativo, y gonos, ángulo: sin ángulo] y las isogónicas. En las primeras la parte roja de la aguja magnética apunta exactamente al norte geográfico y al mismo tiempo al norte magnético porque están alineadas.</p>



<p>Sólo existen dos. En las líneas isogónicas la parte roja de la aguja magnética apunta exclusivamente al norte magnético.</p>



