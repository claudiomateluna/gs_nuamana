<p>Pronto, Jacques Sevin recibe la autorización de Baden-Powell para organizar un campamento-escuela internacional con el mismo mérito que el de Mac-Laren en Inglaterra: esto nos indica la confianza que el fundador puso en esta asociación joven.</p>



<p>El año 1922, es la apertura del campamento internacional de “Chamarande”, para la formación de los jefes.</p>



<figure class="wp-block-image alignleft is-resized"><img src="https://raw.githubusercontent.com/claudiomateluna/nua_mana/gh-pages/uploads/biografia_JacquesSevin_2.png" alt="Jacques Sevin" style="width:360px"/></figure>



<p>Igualmente <strong>se debe al Padre Jacques Sevin</strong> <strong>la cruz potenzada, el ceremonial, la redacción católica de la Ley y Promesa, la redacción de los tres Principios, la oración de los jefes, las letras de algunas canciones scouts (la canción de la Promesa, el himno de las Patrullas, la canción del adiós…)</strong>, así como la creación del escultismo por Extensión dirigida a los discapacitados físicos y mentales.</p>



<p>El trabajo es considerable. En 1924, debe abandonar el título de Comisionado General para dedicarse solamente a las funciones de Comisionado para la formación de los jefes.</p>



<p>Durante 10 años, del mes de agosto de 1923, el Padre dirigirá el campamento de formación de los jefes de Chamarande. Será el animador de él y dará lo mejor para entrenar a jefes dignos del Escultismo católico.</p>



<p>Pero dentro del distrito general, surgen algunas dificultades. Las oposiciones doctrinales provocan una crisis. El 15 de marzo de 1933, el Padre Jacques Sevin pide retirarse. De esta crisis Jacques Sevin señala: “¡Después de haber vivido lo místico del Escultismo, nosotros entramos en la política de Escultismo!”.</p>



<p>El Padre Sevin se retira bajo su tienda, lejos de los campamentos, el consejo y asambleas, lo que le permite lograr un proyecto que durante mucho tiempo llevó en el corazón: la fundación de un Orden religiosa descendiente del Escultismo.</p>



<p>El primer priorato se crea en 1944, bajo el nombre del instituto de la Santa Cruz de Jerusalén. En 1948, crea una escuela scout la “Maison Francaise”.</p>



<figure class="wp-block-image"><img src="https://claudiomateluna.github.io/nuevoBuenaCaza/img/biografias/sevin4.jpg" alt="Jacques Sevin en acampada"/></figure>



<p>En la noche del 19 al 20 de julio de 1951 el Padre Sevin es llamado a la Mansión del Padre. El cuerpo descansa hoy en el cementerio de Boran-sur-Oise donde, anónimamente, algunos exploradores vienen cada año para depositar algunas flores y recitar algunas oraciones en la modesta tumba del fundador de esta caballería de los tiempos modernos. El Padre Jacques Sevin está actualmente en <a href="https://www.cics.org/2021/01/10/avanza-la-causa-de-beatificacion-del-venerable-jaques-sevin/?lang=es" target="_blank" rel="noreferrer noopener">proceso de beatificación</a> en Roma.</p>



